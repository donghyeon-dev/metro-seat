// 호선별 역 목록 가져오기
export function getStationsByLine(lineNumber: LineNumber): Station[] {
  return stations.filter((s) => s.lineNumber === lineNumber);
}

// 역 이름으로 검색
export function searchStations(query: string, lineNumber?: LineNumber): Station[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return stations
    .filter(
      (s) =>
        s.name.toLowerCase().includes(q) &&
        (lineNumber == null || s.lineNumber === lineNumber)
    )
    .slice(0, 20);
}
